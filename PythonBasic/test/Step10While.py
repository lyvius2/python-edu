'''
    - 반복문 while 사용하기
'''

# while문을 탈출하기 위한 제어변수
count1 = 0

while count1<10:
    # 작업하고
    print(count1)
    # 카운트 올리기
    count1 = count1+1
    
print("while 문 탈출 후 count1:", count1)

# 1~100까지의 합을 구해서 출력하고 싶다면
sum=0
count2=0

while count2 < 100:
    count2 = count2 + 1
    sum = sum + count2

print("1~100까지의 합:", sum)

# 값 다시 리셋
sum=0
count2=0

while True:
    count2 = count2+1
    sum = sum + count2
    if count2 == 100:
        break # 가장 가까운 반복문 블럭을 빠져나온다.

print("1~100까지의 합:", sum)

